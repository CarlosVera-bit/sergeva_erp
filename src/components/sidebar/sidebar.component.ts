  // Helper para filtrar items
  private filterItems(items: NavItem[]): NavItem[] {
    const userRole = this.authService.getUserRol();
    if (!userRole) return [];

    // Normalizar rol de trabajador/operativo/operador
    const normalizedRole = (userRole === 'trabajador' || userRole === 'operativo' || userRole === 'operador') ? 'operativo' : userRole;

    return items.filter(item => {
      if (!item.roles) return true;
      return item.roles.includes(normalizedRole) || item.roles.includes(userRole);
    });
  }

  // Items de la sección Principal (Solo Dashboard)
  principalItems = computed(() => {
    const all = this.allNavItems();
    const items = all.filter(item => item.id === 'dashboard');
    return this.filterItems(items);
  });

  // Items de la sección Operaciones (Archivo por Cliente, Cotizaciones, OT, Inventario, Egreso, Proveedores, Trabajadores, Pedidos)
  operationsItems = computed(() => {
    const all = this.allNavItems();
    const ids = ['client-files', 'quotes', 'work-orders', 'inventory', 'egreso-bodega', 'proveedores', 'workers', 'purchasing'];
    const items = ids.map(id => all.find(item => item.id === id)).filter(Boolean) as NavItem[];
    return this.filterItems(items);
  });

  // Items de la sección Registros (Cronograma y Personal, Control de Horas)
  registrosItems = computed(() => {
    const all = this.allNavItems();
    const ids = ['schedule', 'hr'];
    const items = ids.map(id => all.find(item => item.id === id)).filter(Boolean) as NavItem[];
    return this.filterItems(items);
  });

  // Items de la sección Administración (Integración Contable, Reportes)
  adminItems = computed(() => {
    const all = this.allNavItems();
    const ids = ['accounting', 'reports'];
    const items = ids.map(id => all.find(item => item.id === id)).filter(Boolean) as NavItem[];
    return this.filterItems(items);
  });

